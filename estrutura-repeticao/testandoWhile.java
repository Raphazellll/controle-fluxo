public class testandoWhile {

    public static void main(String[] args) {
        
        int numero = 0; //inicializa o contador

        while ( numero <= 100) { //condição para o loop continuar 
                System.out.println(numero); //imprime o valor atual do contador

                numero++; //incrementador
         }
    }
    
}
